coffee_price = 1.50
number_of_coffees = 4

print(coffee_price * number_of_coffees)  # Prints "6.0"
print(coffee_price)  # Prints "1.5"
print(number_of_coffees)  # Prints "4"

# Updating the price
coffee_price = 2.00

print(coffee_price * number_of_coffees)  # Prints "8.0"
print(coffee_price)  # Prints "2.0"
print(number_of_coffees)  # Prints "4"

